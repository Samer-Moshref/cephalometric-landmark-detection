def build_samples(img_dir, ann_dir):
    img_files = os.listdir(img_dir)
    id_to_imgfile = {os.path.splitext(f)[0]: f for f in img_files}
    ann_files = [f for f in os.listdir(ann_dir) if f.endswith('.json')]
    result = []
    for ann_file in ann_files:
        ceph_id = os.path.splitext(ann_file)[0]
        if ceph_id not in id_to_imgfile:
            continue
        with open(os.path.join(ann_dir, ann_file), 'r') as f:
            data = json.load(f)
        coords = {}
        for lm in data['landmarks']:
            if lm['symbol'] in ('A', 'N', 'B'):
                coords[lm['symbol']] = (lm['value']['x'], lm['value']['y'])
        if len(coords) == 3:
            result.append((os.path.join(img_dir, id_to_imgfile[ceph_id]), coords))
    return result

