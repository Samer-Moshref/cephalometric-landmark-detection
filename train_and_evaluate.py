# CELL 5 — Dataset class (with square-padding fix)
# ============================================================
class CephDataset(Dataset):
    def __init__(self, samples, target_size=512):
        self.samples = samples
        self.target_size = target_size
        self.to_tensor = T.ToTensor()

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx):
        img_path, coords = self.samples[idx]
        img = Image.open(img_path).convert('L')
        orig_w, orig_h = img.size

        square_size = max(orig_w, orig_h)
        pad_left = (square_size - orig_w) // 2
        pad_top = (square_size - orig_h) // 2
        pad_right = square_size - orig_w - pad_left
        pad_bottom = square_size - orig_h - pad_top

        img_padded = ImageOps.expand(img, border=(pad_left, pad_top, pad_right, pad_bottom), fill=0)
        img_resized = img_padded.resize((self.target_size, self.target_size))
        img_tensor = self.to_tensor(img_resized)

        scale = self.target_size / square_size

        landmarks = []
        for symbol in ('A', 'N', 'B'):
            x, y = coords[symbol]
            new_x = (x + pad_left) * scale
            new_y = (y + pad_top) * scale
            landmarks.append(new_x)
            landmarks.append(new_y)

        return img_tensor, torch.tensor(landmarks, dtype=torch.float32)

